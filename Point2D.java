package TranChauLAB3;

import java.util.Scanner;

public class Point2D 
{
       float x=0.0f;
       float y=0.0f;
    public Point2D(){
    }
    public Point2D(float x,float y)
    {
        x=this.x;
        y=this.y;
    }

    public float getX() 
    {
        return x;
    }

    public float getY() 
    {
        return y;
    }
    
}